    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        try {
            ReceptionDTO searchCustomer = ManageReceptionController.searchOrder(txtOID.getText());
            if (searchCustomer != null) {
                reservedOrderID = txtOID.getText();
                boolean reserveCustomer = ManageReceptionController.reserveCustomer(reservedOrderID);
                if (reserveCustomer) {
                    txtOID.setText(searchCustomer.getOrderID());
                    txtName.setText(searchCustomer.getCustomerName());
                    txtTp.setText(searchCustomer.getTPNumber());
                    txtQty.setText(searchCustomer.getQty()+"");
                    txtDate.setText(searchCustomer.getDate());
                    txtTime.setText(searchCustomer.getTime());
                } else {
                    JOptionPane.showMessageDialog(this,
                        reservedOrderID + "Customer has been Already Reserved");
                    clearText();
                }
            } else {
                JOptionPane.showMessageDialog(this, "Not Found Customer");
            }
        } catch (Exception ex) {
            Logger.getLogger(ReceptionView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        try {
            String status="going Order";
            ReceptionDTO receptionDTO = new ReceptionDTO(
                txtOID.getText(),
                txtName.getText(),
                txtTp.getText(),
                Integer.parseInt(txtQty.getText()),
                txtDate.getText(),
                txtTime.getText(),
                status
            );
            boolean addReception = ManageReceptionController.addReception(receptionDTO);
            if (addReception) {
                JOptionPane.showMessageDialog(this, "Customer has been Added Successfully");
                loadAllOrder();
                clearText();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to Added Customer");
            }
            ManageReceptionController.releaseCustomer(reservedOrderID);
            reservedOrderID="";
        } catch (Exception ex) {
            Logger.getLogger(ReceptionView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

