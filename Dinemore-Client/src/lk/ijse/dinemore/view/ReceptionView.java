    private void loadAllOrder() {
        DefaultTableModel dtm = (DefaultTableModel) tblOrder.getModel();
        dtm.setRowCount(0);
        try {
            List<ReceptionDTO> receptions = ManageReceptionController.getAllOrder();
            if (receptions == null) {
                return;
            }

            for (ReceptionDTO receptionDTO : receptions) {
                Object[] rowData = {
                    receptionDTO.getOrderID(),
                    receptionDTO.getCustomerName(),
                    receptionDTO.getTPNumber(),
                    receptionDTO.getQty(),
                    receptionDTO.getDate(),
                    receptionDTO.getTime(),
                    receptionDTO.getStatus()
                };
                dtm.addRow(rowData);
            }

        } catch (Exception ex) {
            Logger.getLogger(ReceptionView.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

