    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        try {
            AddReceptionDTO addReceptionDTO=new AddReceptionDTO(
                    txtRecID.getText(),
                    txtRecName.getText(),
                    txtRecNic.getText(),
                    txtRecAdderss.getText()
            );
            boolean addReception=ManageAddReceptionController.addReception(addReceptionDTO);
            if (addReception) {
                JOptionPane.showMessageDialog(this, "Reception has been Added Successfully");
                loadAllReception();
                clearText();
            }
        } catch (Exception ex) {
            Logger.getLogger(AddReceptionView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        try {
            boolean removeReception = ManageAddReceptionController.deleteReception(txtRecID.getText());
            if (removeReception) {
                JOptionPane.showMessageDialog(this, "Reception has been Delete Successfully");
                loadAllReception();
                clearText();
            }else {
                JOptionPane.showMessageDialog(this, "Not Found ");
            }
        } catch (Exception ex) {
            Logger.getLogger(AddReceptionView.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

