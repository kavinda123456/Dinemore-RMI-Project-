    private void loadAllReception() {
        DefaultTableModel dtm = (DefaultTableModel) tblReception.getModel();
        dtm.setRowCount(0);
        try {
            List<AddReceptionDTO> receptions = ManageAddReceptionController.getAllReception();
            if (receptions == null) {
                return;
            }

            for (AddReceptionDTO addReceptionDTO : receptions) {
                Object[] rowData = {
                    addReceptionDTO.getRecID(),
                    addReceptionDTO.getRecName(),
                    addReceptionDTO.getRecNic(),
                    addReceptionDTO.getRecAddress()
                };
                dtm.addRow(rowData);
            }

        } catch (Exception ex) {
            Logger.getLogger(AddReceptionView.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

